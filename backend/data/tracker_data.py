def get_financials(ticker: str):
    """Return historical rows (non-estimated) for a given ticker."""
    rows = TRACKER_DATA.get(ticker.upper(), [])
    historical = []
    for r in rows:
        if not r["estimated"]:
            historical.append({
                "year": r["year"],
                "revenue": r["revenue"],
                "net_profit": r["net_profit"],
                "margin_pct": round(r["pat_margin"] * 100, 2) if r["pat_margin"] is not None else None,
                "valuation_multiple": r["valuation_multiple"],
                "share_price": r["share_price"] if r["share_price"] else None,
                "estimated": False,
            })
    return historical

def get_projections(ticker: str):
    """Return projected rows (estimated) with base/bull/bear variants."""
    rows = TRACKER_DATA.get(ticker.upper(), [])
    base = []
    for r in rows:
        if r["estimated"]:
            base.append({
                "year": r["year"],
                "revenue_base": r["revenue"],
                "revenue_bull": round(r["revenue"] * 1.20, 1),
                "revenue_bear": round(r["revenue"] * 0.80, 1),
                "margin_base": round(r["pat_margin"] * 100, 2) if r["pat_margin"] is not None else None,
                "margin_bull": round(r["pat_margin"] * 100 * 1.3, 2) if r["pat_margin"] is not None else None,
                "margin_bear": round(r["pat_margin"] * 100 * 0.7, 2) if r["pat_margin"] is not None else None,
                "price_base": r["share_price"],
                "price_bull": round(r["share_price"] * 1.25, 1) if r["share_price"] else None,
                "price_bear": round(r["share_price"] * 0.75, 1) if r["share_price"] else None,
                "valuation_multiple": r["valuation_multiple"],
            })
    return base

def get_meta(ticker: str):
    meta = COMPANY_META.get(ticker.upper())
    if not meta:
        return None
    rows = TRACKER_DATA.get(ticker.upper(), [])
    latest = [r for r in rows if not r["estimated"]]
    latest = latest[-1] if latest else {}
    return {
        "ticker": ticker.upper(),
        "name": meta["name"],
        "sector": meta["sector"],
        "exchange": "NSE",
        "multiple_type": meta["multiple_type"],
        "latest_price": latest.get("share_price"),
        "latest_revenue": latest.get("revenue"),
        "market_cap": latest.get("market_cap"),
    }
